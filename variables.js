// dynamic typing: refers to ability to initialize variable to data type
let firstName = 'Joseline';
console.log(typeof firstName);

firstName = 100;
console.log(typeof firstName);

firstName = true;
console.log(typeof firstName);

// object: data type in js that allows you to sort key value pairs, they're a reference type
let course = {
    name: 'Javascript refresh',
    hours: 3
};
console.log(course.name);

// better way to write object
course.name = 'javascript refresh';
console.log(course.name);

// 27min
